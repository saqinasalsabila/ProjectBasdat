    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel deft1 = (DefaultTableModel) jTable1.getModel();
        int SelectIndex = jTable1.getSelectedRow();

        String platNo = platTextField.getText();
        String tipe = tipeTextField.getText();
        String idDriver = deft1.getValueAt(SelectIndex, 0).toString();

        int response = JOptionPane.showConfirmDialog(this, "Are you sure you want to update this record?", "Confirm",
            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (response == JOptionPane.YES_OPTION) {
            try {
                pst = con.prepareStatement("update Kendaraan set Plat_Nomor = ?, Tipe = ? where ID_Driver = ?");
                pst.setString(1, platNo);
                pst.setString(2, tipe);
                pst.setString(3, idDriver);
                int k = pst.executeUpdate();

                if(k==1){
                    JOptionPane.showMessageDialog(this, "Data Updated");
                    platTextField.setText("");
                    tipeTextField.setText("");
                    KendaraanTable();
                    saveButton.setEnabled(true);
                }else{
                    JOptionPane.showMessageDialog(this, "Failed");
                }

            } catch (SQLException ex) {
                Logger.getLogger(Driver.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (response == JOptionPane.NO_OPTION) {
            System.out.println("Cancel");
            platTextField.setText("");
            tipeTextField.setText("");
        }

    }//GEN-LAST:event_updateButtonActionPerformed

