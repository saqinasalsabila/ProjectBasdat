    public void KendaraanTable(){
   
        try {
            pst = con.prepareStatement("SELECT * FROM Kendaraan");
            ResultSet rs = pst.executeQuery();

            ResultSetMetaData rsm = rs.getMetaData();
            int count = rsm.getColumnCount();

            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= count; i++) {
                    v2.add(rs.getString("ID_Driver"));
                    v2.add(rs.getString("Plat_Nomor"));
                    v2.add(rs.getString("Tipe"));
                }
                df.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Kendaraan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

